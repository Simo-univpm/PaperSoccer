package it.unicam.cs.pa.paperSoccer.core;

public class Ball {
	private int x;
	private int y;
	
	
	
	public Ball(int x, int y) {
		this.x=x;
		this.y=y;
	}
	
	
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
	
	
}
